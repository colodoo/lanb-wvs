package com.colodoo.manager.bugLevel.action;

import com.colodoo.manager.bugLevel.model.BugLevel;
import com.colodoo.manager.bugLevel.model.BugLevelVO;
import com.colodoo.manager.bugLevel.service.BugLevelService;
import com.colodoo.framework.utils.Contants;
import com.github.pagehelper.PageInfo;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.CrossOrigin;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.ResponseBody;
import java.util.HashMap;
import java.util.Map;
import java.util.List;

/**
* @author colodoo
* @date 2019-4-14 20:49:28
* @description
*/
@Controller
@RequestMapping(value = "/bugLevel")
@CrossOrigin
public class BugLevelAction {

    @Autowired
    BugLevelService bugLevelService;

	/**
	 * 新增数据
	 * 
	 * @param model
	 * @return
	 */
    @RequestMapping(value = "/save")
    @ResponseBody
    public Map<String, Object> save(@RequestBody BugLevel model) {
        Map<String, Object> rspMap = new HashMap<String, Object>();
        int ret = bugLevelService.saveBugLevel(model);
        if(ret > 0) {
            rspMap.put("success", true);
        } else {
            rspMap.put("msg", Contants.MSG_SAVE_FAIL);
        }
        return rspMap;
    }

	/**
	 * 删除数据
	 * 
	 * @param model
	 * @return
	 */
    @RequestMapping(value = "/delete")
    @ResponseBody
    public Map<String, Object> delete(@RequestBody BugLevel model) {
        Map<String, Object> rspMap = new HashMap<String, Object>();
        int ret = bugLevelService.deleteBugLevel(model);
        if(ret > 0) {
            rspMap.put("success", true);
        } else {
            rspMap.put("msg", Contants.MSG_DELETE_FAIL);
        }
        return rspMap;
    }

	/**
	 * 更新数据
	 * 
	 * @param model
	 * @return
	 */
    @RequestMapping(value = "/update")
    @ResponseBody
    public Map<String, Object> update(@RequestBody BugLevel model) {
        Map<String, Object> rspMap = new HashMap<String, Object>();
        int ret = bugLevelService.updateBugLevel(model);
        if(ret > 0) {
            rspMap.put("success", true);
        } else {
            rspMap.put("msg", Contants.MSG_UPDATE_FAIL);
        }
        return rspMap;
    }

	/**
	 * 根据id查找单条数据
	 * 
	 * @param model
	 * @return
	 */
    @RequestMapping(value = "/queryById")
    @ResponseBody
    public Map<String, Object> queryById(@RequestBody BugLevel model) {
        Map<String, Object> rspMap = new HashMap<String, Object>();
        rspMap.put("rows", bugLevelService.queryById(model));
        return rspMap;
    }

	/**
	 * 查找列表
	 * 
	 * @param model
	 * @return
	 */
    @RequestMapping(value = "/query")
    @ResponseBody
    public List<BugLevel> query(@RequestBody BugLevelVO model) {
        return bugLevelService.query(model);
    }

	/**
	 * 查找分页列表
	 * 
	 * @param model
	 * @return
	 */
    @RequestMapping(value = "/queryPage")
    @ResponseBody
    public Map<String, Object> queryPage(@RequestBody BugLevelVO model) {
        Map<String, Object> rspMap = new HashMap<String, Object>();
        PageInfo<BugLevel> info = bugLevelService.query(model.getPage(), model);
        rspMap.put(Contants.TABLE_ROWS, info.getList());
        rspMap.put(Contants.TABLE_TOTAL, info.getTotal());
        return rspMap;
    }
}
